def normalize_language(lang: str | None) -> str:
    """Return a supported language code."""
    if lang in SUPPORTED_LANGUAGES:
        return lang
    return DEFAULT_LANGUAGE


def translate(key: str, lang: str | None = None) -> str:
    """Translate a key with English fallback."""
    resolved_lang = normalize_language(lang)
    return TRANSLATIONS.get(resolved_lang, {}).get(
        key, TRANSLATIONS[DEFAULT_LANGUAGE].get(key, key)
    )
